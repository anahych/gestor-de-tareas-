    private void crearTareaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearTareaButtonActionPerformed
        // Obtener datos de los campos
        String usuarioAsignado = (String) jComboBox1.getSelectedItem();
        String nombre = nombreTextField.getText().trim();
        String descripcion = descripcionTextField.getText().trim();
        String prioridad = (String) prioridadComboBox1.getSelectedItem();
        String estado = (String) estadoComboBox2.getSelectedItem();
        String fechaInicio = fechaInicioTextField2.getText().trim();
        String fechaEntrega = fechaEntregaTextField.getText().trim();

        // Validar campos
        String mensajeValidacion = validarCampos(nombre, descripcion, fechaInicio, fechaEntrega);
        if (!mensajeValidacion.isEmpty()) {
            JOptionPane.showMessageDialog(this, mensajeValidacion, "Advertencia", JOptionPane.WARNING_MESSAGE);
            return; // Detener el flujo si hay campos vacíos
        }

        // Verificar si la tarea ya existe
        if (tareaYaExiste(usuarioAsignado, nombre)) {
        JOptionPane.showMessageDialog(this, "Ya existe una tarea con este nombre.", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Detener el flujo si ya existe
        }

        //Si pasa las validaciones, guardar usuario
        guardarTarea(usuarioAsignado, nombre, descripcion, prioridad, estado, fechaInicio, fechaEntrega);

        //Limpiar campos después de guardar
        nombreTextField.setText("");
        descripcionTextField.setText("");
        prioridadComboBox1.setSelectedIndex(0);
        estadoComboBox2.setSelectedIndex(0);
        fechaInicioTextField2.setText("");
        fechaEntregaTextField.setText("");
        
        JOptionPane.showMessageDialog(this, "Tarea creada exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_crearTareaButtonActionPerformed

    private boolean tareaYaExiste(String usuarioAsignado, String nombre) {
    
        String archivoTareas = "tareas_" + variablesGlobales.adminUsername + ".txt";
       
        
        try (Scanner scanner = new Scanner(new java.io.File(archivoTareas))) {
        while (scanner.hasNextLine()) {
            String[] tarea = scanner.nextLine().split(",");
            if (tarea.length >= 2 && tarea[1].equalsIgnoreCase(nombre)) {
                return true; // Tarea encontrada
            }
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
    return false; // Tarea no encontrada
    }
    
   
   private void guardarTarea(String usuarioAsignado, String nombre, String descripcion, String prioridad, String estado, String fechaInicio, String fechaEntrega) {
    
    // Archivo específico del administrador y archivo general
    String archivoTareas = "tareas_" + variablesGlobales.getAdminUsername() + ".txt"; 
    String archivoGeneral = "tareasGeneral.txt"; 
    
    // Línea para el archivo general (incluye el nombre del administrador)
    String lineaGeneral = variablesGlobales.getAdminUsername() + "," + usuarioAsignado + "," + nombre + "," + descripcion + "," + prioridad + "," + estado + "," + fechaInicio + "," + fechaEntrega;
    
    // Línea para el archivo específico del administrador (sin el nombre del administrador)
    String lineaTareas = usuarioAsignado + "," + nombre + "," + descripcion + "," + prioridad + "," + estado + "," + fechaInicio + "," + fechaEntrega;

    // Guardar en el archivo específico del administrador
    try (BufferedWriter writer = new BufferedWriter(new FileWriter(archivoTareas, true))) {
        writer.write(lineaTareas); // Guardar solo los datos de la tarea, sin el nombre del administrador
        writer.newLine();
        System.out.println("Tarea guardada en " + archivoTareas + ": " + lineaTareas);
    } catch (IOException e) {
        e.printStackTrace();
    }

    // Guardar en el archivo general (incluye el nombre del administrador)
    try (BufferedWriter writerGeneral = new BufferedWriter(new FileWriter(archivoGeneral, true))) {
        writerGeneral.write(lineaGeneral); // Guardar la línea completa con el nombre del administrador
        writerGeneral.newLine();
        System.out.println("Tarea guardada en archivo general: " + lineaGeneral);
    } catch (IOException e) {
        e.printStackTrace();
    }
}

     
    
