   private void cargarUsuarios() {
    try (Scanner scanner = new Scanner(new java.io.File("usuarios.txt"))) {
        while (scanner.hasNextLine()) {
            String[] usuario = scanner.nextLine().split(",");  // Leer cada línea del archivo y separar por comas

            // Verificar si el rol del usuario es "User" (suponiendo que el rol está en la tercera posición)
            if (usuario.length >= 3 && usuario[2].equalsIgnoreCase("User")) {
                jComboBox1.addItem(usuario[0]); // Agregar al JComboBox solo los usuarios con el rol "User"
            }
        }
    } catch (IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al cargar los usuarios.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}

