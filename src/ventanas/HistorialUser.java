    private void cargarTareasUsuario(String nombreUsuario) {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0); // Limpiar la tabla

        String archivoTareas = "tareasGeneral.txt"; // Archivo con las tareas

        try (BufferedReader reader = new BufferedReader(new FileReader(archivoTareas))) {
            String linea;

            while ((linea = reader.readLine()) != null) {
                // Suponiendo formato: admin, usuarioAsignado, nombreTarea, descripcion, prioridad, estado, fechaInicio, fechaEntrega
                String[] datos = linea.split(",");
                if (datos.length == 8 && datos[1].equalsIgnoreCase(nombreUsuario)) {
                    // Agregar solo nombreTarea, estado y fechaEntrega
                    modelo.addRow(new Object[]{datos[2], datos[5], datos[7]});
                }
            }
        } catch (FileNotFoundException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Archivo no encontrado.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        } catch (IOException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error al leer el archivo.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }
