    private void entregarTareaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entregarTareaButtonActionPerformed
        // Obtener la fila seleccionada
        int filaSeleccionada = jTable1.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione una tarea para entregar.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Obtener los datos de la fila seleccionada
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        String asignadoPor = modelo.getValueAt(filaSeleccionada, 0).toString();
        String nombreTarea = modelo.getValueAt(filaSeleccionada, 1).toString();
        String descripcion = modelo.getValueAt(filaSeleccionada, 2).toString();
        String prioridad = modelo.getValueAt(filaSeleccionada, 3).toString();
        String estado = modelo.getValueAt(filaSeleccionada, 4).toString();
        String fechaEntrega = modelo.getValueAt(filaSeleccionada, 5).toString();

        // Obtener el usuario actual
        String usuarioActual = variablesGlobales.usuarioActual;

        // Guardar la tarea en "tareasEntregadas.txt"
        String tareaEntregada = usuarioActual + "," + asignadoPor + "," + nombreTarea + "," + descripcion + "," + prioridad + "," + estado + "," + fechaEntrega;
        try (FileWriter writer = new FileWriter("tareasEntregadas.txt", true)) {
            writer.write(tareaEntregada + "\n");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar la tarea entregada.", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
            return;
        }

        // Modificar el estado de la tarea a "Entregada"
        if (actualizarArchivoTareas(asignadoPor, nombreTarea, descripcion, prioridad, fechaEntrega, "Entregada")) {
            // Eliminar la tarea de la tabla
            modelo.removeRow(filaSeleccionada);
            JOptionPane.showMessageDialog(this, "Tarea entregada exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Error al actualizar el archivo de tareas.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private boolean actualizarArchivoTareas(String asignadoPor, String nombreTarea, String descripcion, String prioridad, String fechaEntrega, String nuevoEstado) {
        File archivoOriginal = new File("tareasGeneral.txt");
        File archivoTemporal = new File("tareasGeneral_temp.txt");
        boolean tareaActualizada = false;

        try (BufferedReader reader = new BufferedReader(new FileReader(archivoOriginal));
             BufferedWriter writer = new BufferedWriter(new FileWriter(archivoTemporal))) {

            String linea;
            while ((linea = reader.readLine()) != null) {
                String[] datos = linea.split(",");
                if (datos.length == 8 &&
                    datos[0].equals(asignadoPor) &&
                    datos[2].equals(nombreTarea) &&
                    datos[3].equals(descripcion) &&
                    datos[4].equals(prioridad) &&
                    datos[7].equals(fechaEntrega)) {
                    // Modificar el estado de la tarea al nuevo estado
                    datos[5] = nuevoEstado;
                    tareaActualizada = true;
                }
                // Escribir la línea (ya modificada si corresponde)
                writer.write(String.join(",", datos) + "\n");
            }
        } catch (IOException e) {
            e.printStackTrace();
            return false;
        }

        // Reemplazar el archivo original con el temporal
        if (archivoOriginal.delete() && archivoTemporal.renameTo(archivoOriginal)) {
            return tareaActualizada;
        } else {
            return false;
        }
    

    }//GEN-LAST:event_entregarTareaButtonActionPerformed

