    public void cargarTareasUsuario(String nombreUsuario) {
        // Modelo de la tabla
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0); // Limpiar la tabla antes de llenarla

        String archivoTareas = "tareasGeneral.txt"; // Archivo con todas las tareas

        try (Scanner scanner = new Scanner(new File(archivoTareas))) {
            while (scanner.hasNextLine()) {
                String linea = scanner.nextLine();
                String[] datos = linea.split(","); // Suponiendo formato: admin, usuarioAsignado, nombre, descripcion, prioridad, estado, fechaInicio, fechaEntrega

                if (datos.length == 8 &&
                    datos[1].equalsIgnoreCase(nombreUsuario) && 
                    !datos[5].equalsIgnoreCase("Entregada")) { // Excluir tareas entregadas
                    modelo.addRow(new Object[]{
                        datos[0], // Nombre del administrador
                        datos[2], // Nombre de la tarea
                        datos[3], // Descripción
                        datos[4], // Prioridad
                        datos[5], // Estado
                        datos[7]  // Fecha de entrega
                    });
                }
            }
        } catch (FileNotFoundException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "No se pudo encontrar el archivo de tareas.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    


